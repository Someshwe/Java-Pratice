
public class Index{
    public static void main(String []args)
    {
        int i=26;
        System.out.println(i);
        System.out.println(--i);
        System.out.println(i++);
        System.out.println(--i);
        System.out.println(++i);
        System.out.println(i--);
        System.out.println(--i);
        System.out.println(i++);
        System.out.println(++i);
        System.out.println(i--);
        System.out.println(i--);
        System.out.println(--i);
        System.out.println(i);      //call the method
    }
}
